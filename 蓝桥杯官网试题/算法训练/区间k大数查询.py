'''
问题描述
给定一个序列，每次询问序列中第l个数到第r个数中第K大的数是哪个。

输入格式
第一行包含一个数n，表示序列长度。

第二行包含n个正整数，表示给定的序列。

第三个包含一个正整数m，表示询问个数。

接下来m行，每行三个数l,r,K，表示询问序列从左往右第l个数到第r个数中，从大往小第K大的数是哪个。序列元素从1开始标号。

输出格式
总共输出m行，每行一个数，表示询问的答案。
样例输入
5
1 2 3 4 5
2
1 5 2
2 3 2
样例输出
4
2
'''
# 输入数据
n = int(input())
list_q = list(map(int, input().split()))
m = int(input())

for _ in range(m):
    l,r,k = map(int, input().split())
    ans = list_q[l-1:r]
    ans = sorted(ans, reverse=True)
    print(ans[k-1])
